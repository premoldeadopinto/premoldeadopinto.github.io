function cargarProducto() {
    const params = new URLSearchParams(window.location.search);
    const productoId = params.get('producto');
    const producto = productos[productoId];

    if (producto) {
        if (productosConDiseno2.includes(productoId)) {
            // Mostrar diseño 2
            document.querySelector('.detalle-contenido').style.display = 'none';
            document.getElementById('detalle-contenido2').style.display = 'flex';

            document.getElementById('nombre-producto2').textContent = producto.nombre;
            document.getElementById('precio-producto2').textContent = producto.precio || '';
            document.getElementById('descripcion-producto2').textContent = producto.descripcion || '';

            const galeria = document.getElementById('galeria-horizontal');
            galeria.innerHTML = '';
            const textos = textosImagenes[productoId] || [];
            producto.imagenes.forEach((src, idx) => {
                const item = document.createElement('div');
                item.className = 'galeria-item';
                const img = document.createElement('img');
                img.src = src;
                img.alt = producto.nombre;
                const texto = document.createElement('div');
                texto.className = 'texto-imagen';
                texto.textContent = textos[idx] || producto.descripcion || '';
                item.appendChild(img);
                item.appendChild(texto);
                galeria.appendChild(item);
            });
        } else {
            // Diseño clásico
            document.querySelector('.detalle-contenido').style.display = 'flex';
            document.getElementById('detalle-contenido2').style.display = 'none';

            document.getElementById('nombre-producto').textContent = producto.nombre;
            document.getElementById('precio-producto').textContent = producto.precio || '';
            document.getElementById('descripcion-producto').textContent = producto.descripcion || '';

            const miniaturas = document.getElementById('miniaturas');
            miniaturas.innerHTML = '';
            producto.imagenes.forEach(src => {
                const img = document.createElement('img');
                img.src = src;
                img.alt = "Miniatura";
                img.onclick = () => cambiarImagen(src);
                miniaturas.appendChild(img);
            });

            cambiarImagen(producto.imagenes[0]);
        }
    }
}

function cambiarImagen(src) {
    document.getElementById('imagen-grande').src = src;
}

function volver() {
    window.history.back();
}

document.addEventListener("DOMContentLoaded", cargarProducto); 